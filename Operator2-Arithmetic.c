#include<stdio.h>
void main()
{
    int a,b,c,d,e,f,g,h;
    // User Input
    printf("Enter two number :");
    scanf("%d %d",&a,&b);
    // Arithmetic operations
    d = a+b;
    e = a-b;
    f = a*b;
    g = a/b;
    h = a%b;
    // Output
    printf("Addition is : %d\n", d);
    printf("Subtraction is : %d\n", e);
    printf("Multiplication is : %d\n", f);
    printf("Division is : %d\n", g);
    printf("Reminder is : %d\n", h);
}


